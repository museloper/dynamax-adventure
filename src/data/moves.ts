export function moveList(names: string[]): Move[] {
  return names.map((name) => {
    const trimmed = name.trim();
    const info = MOVES[trimmed] ?? FALLBACK;
    return {
      nameKo: trimmed,
      type: info.type,
      category: info.category,
      power: info.power,
    };
  });
}
